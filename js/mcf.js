function filterSelection() {
  var x, i, input, filter;
  x = document.getElementsByClassName('poster');
  input = document.getElementById('search-input');
  filter = input.value.toLowerCase();
  if (filter == 'all') filter = '';
  // Add the "show" class (display:block) to the filtered elements, and remove the "show" class from the elements that are not selected
  for (i = 0; i < x.length; i++) {
    w3RemoveClass(x[i], 'showPoster');
    if (x[i].className.indexOf(filter) > -1) w3AddClass(x[i], 'showPoster');
  }
}

// Show filtered elements
function w3AddClass(element, name) {
  var i, arr1, arr2;
  arr1 = element.className.split(' ');
  arr2 = name.split(' ');
  for (i = 0; i < arr2.length; i++) {
    if (arr1.indexOf(arr2[i]) == -1) {
      element.className += ' ' + arr2[i];
    }
  }
}

// Hide elements that are not selected
function w3RemoveClass(element, name) {
  var i, arr1, arr2;
  arr1 = element.className.split(' ');
  arr2 = name.split(' ');
  for (i = 0; i < arr2.length; i++) {
    while (arr1.indexOf(arr2[i]) > -1) {
      arr1.splice(arr1.indexOf(arr2[i]), 1);
    }
  }
  element.className = arr1.join(' ');
}

// Show all posters for movies and series
function showall() {
  var texthtml = '';

  // Sort poster array form newer to olders
  var postersSorted = posters.sort((a, b) => (a.year < b.year ? 1 : -1));

  // Create elements for movie posters based on the array
  for (i = 0; i < postersSorted.length; i++) {
    texthtml += `<div class="col-sm-6 col-md-6 col-lg-3 mb-5 poster ${postersSorted[i].class}">
                      <div class="moviesseries-item mx-auto">
                          <a href="${postersSorted[i].link}" target="_blank">
                              <div
                                  class="moviesseries-item-caption d-flex align-items-center justify-content-center h-100 w-100">
                                  <div class="moviesseries-item-caption-content text-center text-white">
                                      <h1>${postersSorted[i].name}</h1>
                                      <p>${postersSorted[i].year}</p>
                                      <p>${postersSorted[i].description}</p>
                                  </div>
                              </div>
                          </a>
                          <img class="img-fluid" src="assets/img/moviesseries/${postersSorted[i].img}" alt="" />
                      </div>
                  </div>`;
  }

  //Append elements to the specific div on the page
  document.getElementById('allSection').innerHTML = texthtml;
}
